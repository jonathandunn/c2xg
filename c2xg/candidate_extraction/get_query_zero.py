#FUNCTION: get_query_zero --------------------------------------------------------------------#
#INPUT: List of columns to check for equivalence ---------------------------------------------#
#OUTPUT: String of query ---------------------------------------------------------------------#
#---------------------------------------------------------------------------------------------#
def get_query_zero(column_names):
		
	query = ""
	
	for i in range(len(column_names)):
		
		if i == 0:
			query = "(c" + str(i+1) + " != 0 "
			
		elif i  > 0:
			query += " and " + "c" + str(i+1) + " != 0"
	
	query += ")"
	
	return query
