#---------------------------------------------------------------------------------------------#
#FUNCTION: get_query -------------------------------------------------------------------------#
#INPUT: List of columns to check for equivalence ---------------------------------------------#
#OUTPUT: String of query ---------------------------------------------------------------------#
#---------------------------------------------------------------------------------------------#
def get_query(column_names):
		
	query = ""
	
	for i in range(len(column_names)):
		
		if i == 0:
			query = "(" + str(column_names[i]) + " == "
			
		elif i != len(column_names) - 1:
			query += str(column_names[i]) + " == "
		
		elif i == len(column_names) - 1:
			query += str(column_names[i]) + ")"
		
	return query
